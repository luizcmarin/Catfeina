package com.marin.catfeina.ui.main

/**
 * Representa os possíveis estados da interface do usuário durante o processo de sincronização.
 */
sealed interface SyncState {
    /** O processo está inativo, aguardando uma ação do usuário. */
    object Ocioso : SyncState

    /** O processo de sincronização (download, descompactação, etc.) está em andamento. */
    object Executando : SyncState

    /** O processo foi concluído com sucesso. */
    data class Sucesso(val mensagem: String) : SyncState

    /** O processo falhou. */
    data class Falha(val erro: String) : SyncState
}